package com.example.clinic_seg2105;

public class User {
    private String id;
    private String name;
    private String email;
    private String role;

    public User(){

    }

    public User(String id, String name, String email, String role) {
        this.id = id;
        this.name = name;
        this.email = email;
        this.role = role;
    }
}
